class User {
  final String userId;
  final String firstName;
  final String lastName;
  final String gender;
  String imageUrl;
  String email;
  String username;
  String password;

  User(
    this.userId,
    this.firstName,
    this.lastName,
    this.gender,
    this.imageUrl,
    this.email,
    this.username,
    this.password,
  );
}
